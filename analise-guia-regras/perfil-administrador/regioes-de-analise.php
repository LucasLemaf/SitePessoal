<h2>Perguntas e respostas frequentes</h2>
<div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
  <div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingOne">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
          O que é uma região de análise?
        </a>
      </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingOne">
      <div class="panel-body">
        <p>As <strong>Regiões de Análise</strong> permitem a extratificação dos estados, de forma a organizar e facilitar a distribuição dos processos para os Gerentes Operacionais.</p>
        <p>Cada <strong>Região de Análise</strong> é composta por um ou mais municípios do mesmo estado, podendo possuir um ou mais Gerentes Operacionais vinculados, sendo estes, os responsáveis pela tramitação dos processos para análise.</p>
      </div>
    </div>
  </div>
  <div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingTwo">
      <h4 class="panel-title">
        <a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
          O que é a região de análise padrão?
        </a>
      </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
      <div class="panel-body">
        <p>Todo estado possui sua <strong>Região Padrão</strong>. É a região inicial que abrange todos os municípios do estado. Somente à partir da criação de novas regiões, os municípios podem ser removidos da Região Padrão e transferidos às novas regiões criadas.</p>
        <p><i><strong>Exemplo:</strong> um estado com 100 municípios possui inicialmente somente uma região, a Região Padrão (contendo os 100). Ao ser criada uma nova região, todos os municípios que lhe forem adicionados, sairão da Região Padrão. Portanto, se forem adicionados 20 municípios à região criada, a Região Padrão, antes com 100 municípios, terá agora 80.</i></p>
      </div>
    </div>
  </div>
</div>