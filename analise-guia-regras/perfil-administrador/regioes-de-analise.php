<h3><a name="cadastrar-regiao-de-analise"></a>Cadastrar região de análise:</h3>

<ol>
  <li>Acesse o botão <strong>Cadastrar Região de Análise</strong>;</li>
  <li>Informe o <strong>Nome da Região</strong>;</li>
  <li>O campo <strong>UF</strong> virá bloqueado para o estado do perfil;</li>
  <li>Informe os <strong>Municípios</strong> que deseja adicionar à região;</li>
  <li>Acesse o botão <strong>Cadastrar</strong>.</li>
</ol>

<h3><a name="vincular-desvincular-go-regiao-de-analise"></a>Vincular/desvincular GO à uma região de análise:</h3>

<ol>
  <li>Acesse o botão <strong>Ações</strong> da região desejada;</li>
  <li>Escolha a opção <strong>Vinculação de GO</strong>;</li>
  <li>Será aberta uma janela contendo todos os detalhes sobre a <strong>Região de Análise</strong>.</li>
</ol>

<h3><a name="visualizar-detalhes-das-regioes-de-analise"></a>Visualizar detalhes das regiões de análise:</h3>

<p>

<ol>
  <li>Acesse o botão <strong>Ações</strong> da região desejada;</li>
  <li>Escolha a opção <strong>Visualizar</strong>;</li>
  <li>Será aberta uma janela contendo todos os detalhes sobre a <strong>Região de Análise</strong>.</li>
</ol>

