<h3><a name="tecnicos-responsaveis"></a>Técnicos Responsáveis</h3>
<p>Exibe os dados dos técnicos que compõe atualmente a equipe técnica de análise do processo:</p>
<ul>
  <li>Nome</li>
  <li>CPF</li>
</ul>
<br>

<h3><a name="historico-da-analise"></a>Histórico da análise</h3>
<p>Exibe os dados do histórico da análise atual:</p>
<ul>
  <li><strong>Data de envio do car:</strong> dia em que o processo foi enviado para a base de dados do SICAR.</li>
  <li><strong>Data da primeira vinculação da equipe:</strong> dia em que foi realizada a primeira vinculação de técnicos à equipe pelo Gerente Operacional.</li>
  <li><strong>Data do início da análise técnica:</strong> dia em que o primeiro técnico da equipe iniciou análise.</li>
  <li><strong>Data de finalização da análise técnica:</strong> dia em que o técnico líder finalizou a análise.</li>
  <li><strong>Data de conclusão da análise técnica:</strong> dia em que o técnico líder concluiu a análise.</li>
  <li><strong>Duração da análise:</strong> tempo total de duração da análise (do início da análise pelo primeiro ténico ao momento da conclusão da mesma pelo técnico líder).</li>
</ul>
<br>

<h3><a name="historico-do-processo"></a>Histórico do processo</h3>
<p>Exibe os dados referentes ao histórico do processo (SICAR):</p>
<ul>
  <li>Data</li>
  <li>Descrição</li>
</ul>
<br>

<h3><a name="historico-da-tramitacao"></a>Histórico da tramitação</h3>
<p>Exibe os dados referentes às tramitações da análise atual:</p>
<ul>
  <li>Data</li>
  <li>Hora</li>
  <li>Ação</li>
</ul>
<br>

<h3><a name="analises-anteriores"></a>Análises Anteriores</h3>
<p>Exibe os dados e documentos resultantes das análises anteriores do processo:</p>
<ul>
  <li><strong>Parecer Técnico:</strong> documento PDF com o Parecer Técnico gerado pela equipe técnica.</li>
  <li><strong>Relatório de Análise:</strong> documento PDF com o Relatório de Análise gerada pelo Gerente Operacional.</li>
  <li><strong>Dados Análise:</strong> exibe os dados referente às análises anteriores.</li>
</ul>
<br>


