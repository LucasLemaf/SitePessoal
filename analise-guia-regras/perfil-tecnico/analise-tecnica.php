    <div class="panel-heading" role="tab" id="headingOne">
      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
          Por que não encontro um processo?
        </a>
      </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingOne">
      <div class="panel-body">
        Há duas opções para que um processo do qual você tenha iniciado analise não se encontre mais na etapa 'Análise Técnica': (i) o técnico líder concluiu a análise e enviou para o Gerente Operacional validá-la ou (ii) você foi desvinculado da equipe técnica do processo.
      </div>
    </div>
  </div>
  <div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingTwo">
      <h4 class="panel-title">
        <a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
          Por que um dos meus processos na listagem está com sua linha marcada na cor verde?
        </a>
      </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
      <div class="panel-body">
        <p>A linha marcada na cor verde indica que a análise do processo foi finalizada pelo técnico líder, porém ainda não conluída. Isto é, a análise foi finalizada, não podendo mais os técnicos indicarem inconsistências ou alterarem qualquer análise já feita, ficando pendente somente da conclusão pelo técnico líder, momento no qual o processo é enviado para validação do Gerente Operacional.</p>
        Obs: a finalização da análise pode ser desfeita pelo técnico líder antes da conclusão.
      </div>
    </div>
  </div>
