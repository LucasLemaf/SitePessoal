<h3><a name="visualizar_detalhes"></a>Visualizar detalhes do imóvel</h3>
<ol>
  <li>Acesse o botão <strong>Ações » Detalhes do Imóvel</strong> do processo desejado.</li>
</ol>

<h3><a name="visualizar_despacho"></a>Visualizar despacho</h3>
<ol>
  <li>Acesse o botão <strong>Ações » Visualizar Despacho</strong> do processo desejado.</li>
</ol>

<h3><a name="solicitar_desvinculacao"></a>Solicitar desvinculação</h3>
<ol>
  <li>Acesse o botão <strong>Ações » Solicitar Desvinculação</strong> do processo desejado;</li>
  <li>Selecione o <strong>motivo</strong>;</li>
  <li>Informe a <strong>justificativa</strong>;</li>
  <li>Acesse o botão <strong>Solicitar</strong>.</li>
</ol>

<h3><a name="continuar_analise"></a>Continuar Análise</h3>
<ol>
  <li>Acesse o botão <strong>Ações » Continuar Análise</strong> do processo desejado;</li>
</ol>

<hr>

<h3><a name="filtrar_processos"></a>Filtrar Processos</h3>
<ol>
  <li>Acesse o painel <strong>Filtrar Processos</strong>;</li>
  <li>Informe os campos necessários que deseja filtrar;</li>
  <li>Acesse o botão <strong>Pesquisar</strong>.</li>
</ol>

